<h4>How to use this AFT</h4>
<ul>
	<li>Select EP or TN</li>
	<li>Select exchange programme</li>
	<li>The page will refresh to show Sub Product and Master Issue</li>
	<li>Select wether you want to see forms which have the SP/IS as only primary or primary plus secondary (primary will be more accurate filtration as it is the preferred SP/IS in TN/EP form and the secondary SP/IS is the secondary choice)</li>
	<li>If you know exactly what background you want in the SP/IS then select the same</li>
	<li>If you have a duration in mind select the filter if it the start and end date are more important select the same</li>
</ul>
<hr>
<h4>For Business Intelligence</h4>
<ul>
	<li>Understand the forms you have in a programme and then SP/IS</li>
	<li>Select the supply filters in a manner so that you are able to determine exact countries/LC's you can match with for the current forms you have</li>
	<li>If you are going into a raising drive use the same to understand what is available as supply for the focus SP/IS and then raise accordingly</li>
</ul>
